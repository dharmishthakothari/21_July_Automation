package first;

public class DoExample {
	public static void main(String[] args) {
		int i=10;
		do
		{
			System.out.println("Hello "+i);
			i++;
			
		}while(i<=5);
		System.out.println("WITH WHILE ");
		i=10;
		//10<=5
		while(i<=5)
		{
			System.out.println("Hello "+i);
			i++;
			
		}
		
		
		
		
		
	}

}
